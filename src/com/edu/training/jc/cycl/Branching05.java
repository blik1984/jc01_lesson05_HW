/**
 * Составить программу: определения наименьшего из двух чисел а и b.
 */

public class Branching05 {

	public static void main(String[] args) {
		
		int num1 = -455;
		int num2 = 8741618;

		if (num1 < num2) {
			System.out.println("Число " + num1 + " наименьшее");
		} else {
			System.out.println("Число " + num2 + " наименьшее");
		}
	}

}
